package com.fngry.devtool.debug;

/**
 * @author gaorongyu
 */
public class MyGenericObj<S, T, R> {

    S s;
    T t;
    R r;

    public S getS() {
        return s;
    }

    public void setS(S s) {
        this.s = s;
    }

    public T getT() {
        return t;
    }

    public void setT(T t) {
        this.t = t;
    }

    public R getR() {
        return r;
    }

    public void setR(R r) {
        this.r = r;
    }

}
